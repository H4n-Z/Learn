public class Logik {
    public static void main(String[] args) {

        boolean a1;
        boolean z1 = true;
        boolean z2 = false;
    
        a1 = !z2;

        System.out.println(a1);   

        a1 = z1 && z2;

        System.out.println(a1);

        a1 = z1 || z2;

        System.out.println(a1);

        a1 = z1 ^ z2;

        System.out.println(a1);  
    }
}
