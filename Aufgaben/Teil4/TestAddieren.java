package Aufgaben.Teil4;

public class TestAddieren {
    public static void main (String[] args){
        int wert1;
        int wert2;
        wert1 = 3;
        wert2 = 5;

        int summe;

        summe = wert1 + wert2;

        System.out.println(wert1 + " + " + wert2 + " = " + summe);

        int produkt;

        produkt = wert1 * wert2;

        System.out.println(wert1 + " * " + wert2 + " = " + produkt);
    }
}
