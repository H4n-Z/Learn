package Aufgaben.Teil3;

public class Person {
    private String vorname;
    private String name;

    public void setName (String n){
        name = n;
    }

    public void setVorName (String vn){
        vorname = vn;
    }

    public String getName (){
        return name;
    }

    public String getVorName (){
        return vorname;
    }
}
