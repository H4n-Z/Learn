package Aufgaben.Teil5.aufgabe3;

public class Person {
    private String vorname;
    private String name;

    public void setName (String name){
        this.name = name;
    }

    public void setVorName (String vorname){
        this.vorname = vorname;
    }

    public String getName (){
        return name;
    }

    public String getVorName (){
        return vorname;
    }
}
